            <script>
                function openNav() {
                    document.getElementById("side-menu-wrapper-web").className = "active";
                    document.getElementById("sidebarOverlay").className = "fade-in";
                }

                function closeNav() {
                    document.getElementById("side-menu-wrapper-web").className = "";
                    document.getElementById("sidebarOverlay").className = "fade-out";
                }

                function toggleNav() {
                    if (document.getElementById("side-menu-wrapper-web").classList.contains('active')) {
                        closeNav();
                    } else {
                        openNav();
                    }
                }

                function openCollapsible() {
                    that.className = "active";
                }

                function closeCollapsible() {
                    that.className = "";
                }

                function toggleCollapsible(that) {
                    if (that.classList.contains('open')) {
                        closeCollapsible(that);
                    } else {
                        openCollapsible(that);
                    }
                }
                jQuery(document).ready(function($) {
                    $(".collapsible-header .blick-forward").on("click", function() {
                        var _parent = $(this).closest('.collapsible-container');
                        _parent.find(".collapsible-title").toggleClass("closed open");
                        _parent.find(".blick-forward").toggleClass("closed open");
                        _parent.find(".collapsible-content").toggleClass("closed open");
                    });
                });

